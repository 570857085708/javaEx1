package Day6_231211;

public class Calculator {                     

	// 함수생성
	// 이름 : sum , 파라미터 : 정수형2개받기 , 리턴 : 파라미터로 받은 2개의 값을 더해서 리턴
	
	int sum(int x, int y) {
		return x+y;
	}
	
	int sum(int x, int y, int z) {
		return x+y+z;
	}
	
	double sum(int x, double y) {
		return x+y;
	}
	
	
	
	
	
}
