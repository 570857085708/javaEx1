package Day9_231214;

public class  Calculator {
 
	// 1. multifle 메소드 생성
	// 	1-1 int형 숫자 2개 받아서 곱한 후 리턴
	// 	1-2 int형 숫자 3개 받아서 곱한 후 리턴
	//	1-3 double형 숫자 2개 받아서 곱한 후 리턴
	
	
	static int multifle(int x, int y){
		return x * y;
	}
	int multifle(int x, int y, int z){
		return x * y * z;
	}
	static double multifle(double x, double y){
		return x * y;
	}
	
	// calculator 클래스의 multiple 메소드들을 static으로 바꾸고
	// 메인함수에서 클래스명으로 호출해보기
	
	
} //
